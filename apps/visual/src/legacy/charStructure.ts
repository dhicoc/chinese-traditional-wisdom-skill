/** 推断未收录字的结构（按偏旁位置粗判） */
function inferStructure(char: string): CharStructure {
  if (!char || char.length < 1) return '未知';
  const c = char[0];
  // 含围框
  if (['囗', '门'].some((r) => c.includes(r))) return '包围';
  // 上部偏旁 → 上下
  if (['艹', '宀', '广', '雨', '竹', '疒'].some((r) => c.startsWith(r) || c.includes(r))) return '上下';
  // 左侧偏旁 → 左右
  if (['氵', '扌', '忄', '亻', '钅', '衤', '饣', '木', '火', '土', '女', '马', '车', '舟', '足', '言', '虫'].some((r) => c.startsWith(r) || c.includes(r))) return '左右';
  // 单画或多画独体字（无明确偏旁组合）
  if (c.length === 1 || /[一-鿿]/.test(c) && !c.includes('、')) {
    // 简单启发：笔画少且无上下左右偏旁组合 → 独体
    return '独体';
  }
  return '未知';
}

/** 主入口：取汉字结构 + 偏旁象义 */
export function analyzeCharStructure(char: string): CharStructureInfo {
  const c = (char ?? '').trim()[0] ?? '';
  const structure = STRUCTURE_TABLE[c] ?? inferStructure(c);
  const radical = extractRadical(c);
  return {
    structure,
    symbolism: structureSymbolism(structure),
    radical,
    radicalSymbolism: RADICAL_SYMBOLISM[radical] ?? '偏旁象义未收录',
  };
}
