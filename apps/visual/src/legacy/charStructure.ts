// ─── 偏旁提取（取字的首部/常见偏旁）─────────────────
function extractRadical(char: string): string {
  if (!char || char.length < 1) return '';
  const c = char[0];
  // 常见偏旁直接判断（左侧/上部）
  const commonRadicals = ['氵', '灬', '扌', '忄', '亻', '钅', '衤', '饣', '疒', '艹', '宀', '广', '门', '言', '走', '足', '雨', '虫', '木', '火', '土', '金', '日', '月', '口', '心', '手', '目', '耳', '女', '子', '人', '田', '山', '石', '马', '鸟', '鱼', '车', '舟', '风', '草', '竹', '衣', '食', '病', '禾', '水'];
  // 先查多画偏旁（避免「日」被「氵」抢先这类问题按位置）
  for (const r of commonRadicals) {
    if (c.includes(r)) return r;
  }
  return c; // 无匹配返回首字符
}

