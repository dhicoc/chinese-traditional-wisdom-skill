  /** 查询单个术语的通俗解释 */
  explain(term) {
    const exp = this.termExplanations[term];
    if (exp) return exp;
    // 模糊匹配：如果 term 包含在某个 key 中
    for (const [key, val] of Object.entries(this.termExplanations)) {
      if (term.includes(key) || key.includes(term)) return val;
    }
    return "暂无该术语的解释";
  },

