  /** 计算五行生克关系: 0=同, 1=生我, 2=我生, 3=克我, 4=我克 */
  wuxingRelation(me, other) {
    const cycle = ["木","火","土","金","水"];
    const mi = cycle.indexOf(me), oi = cycle.indexOf(other);
    if(mi<0||oi<0) return -1;
    if(mi===oi) return 0; // 同
    // 生我: 我前一位
    if((mi+4)%5 === oi) return 1;
    // 我生: 我后一位
    if((mi+1)%5 === oi) return 2;
    // 克我: 我前两位
    if((mi+3)%5 === oi) return 3;
    // 我克: 我后两位
    if((mi+2)%5 === oi) return 4;
    return -1;
  },

