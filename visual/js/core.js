  /** 为 Canvas 元素绑定 tooltip 交互 */
  bindCanvasTooltip(canvasId, hitTestFn) {
    const canvas = document.getElementById(canvasId);
    if (!canvas) return;
    const tip = this.initTooltip();
    let hideTimer = null;

    canvas.addEventListener("mousemove", (e) => {
      const rect = canvas.getBoundingClientRect();
      const mx = e.clientX - rect.left;
      const my = e.clientY - rect.top;
      const result = hitTestFn(mx, my);
      if (result) {
        const exp = this.explain(result.term);
        // 如果 hitTest 返回了自定义解释, 优先使用
        const text = result.explanation || 
          (result.term ? `<b>${result.term}</b>：${exp}` : result.text || "");
        tip.innerHTML = text;
        tip.style.display = "block";
        tip.style.opacity = "1";
        // 定位: 在鼠标右下方，避免遮挡
        let tx = e.clientX + 16;
        let ty = e.clientY + 12;
        // 防止超出右边界
        if (tx + 330 > window.innerWidth) tx = e.clientX - 330;
        // 防止超出下边界
        if (ty + 200 > window.innerHeight) ty = window.innerHeight - 210;
        if (tx < 4) tx = 4;
        if (ty < 4) ty = 4;
        tip.style.left = tx + "px";
        tip.style.top = ty + "px";
        if (hideTimer) { clearTimeout(hideTimer); hideTimer = null; }
      } else {
        if (!hideTimer) {
          hideTimer = setTimeout(() => { tip.style.display = "none"; hideTimer = null; }, 80);
        }
      }
    });

    canvas.addEventListener("mouseleave", () => {
      tip.style.display = "none";
      if (hideTimer) { clearTimeout(hideTimer); hideTimer = null; }
    });
  },

