def tiangan_index(tg):
    return TIANGAN.index(tg) if tg in TIANGAN else -1

def dizhi_index(dz):
    return DIZHI.index(dz) if dz in DIZHI else -1

def wuxing_relation(e1, e2):
    """Return the relation between two five elements."""
    if e1 == e2:
        return '比和'
    if WUXING_SHENG.get(e1) == e2:
        return '生我'
    if WUXING_SHENG.get(e2) == e1:
        return '我生'
    if WUXING_KE.get(e1) == e2:
        return '克我'
    if WUXING_KE.get(e2) == e1:
        return '我克'
    return ''

def get_sexagenary_index(gan, zhi):
    """Get index (0-59) of a sexagenary pair."""
    target = gan + zhi
    for i, s in enumerate(SEXAGENARY):
        if s == target:
            return i
    return -1

def sexagenary_by_index(idx):
    return SEXAGENARY[idx % 60]
